using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathTask
{
    public class Geometry
    {
        public int RectangleArea(int a, int b)
        {
            return a * b;
        }
    }
    public class GeometryBarrel
    {
        public double BarrelArea(double pi, double b, double c)
        {
            return pi * b * c;
        }
    }
}
